#!/usr/bin/python3

"""
Module demonstrating the use of lists.

This module creates a list of horsemen and checks for the presence of certain elements.

Usage:
    Run the code to create a list of horsemen and check for their presence.
"""

# Create a list of horsemen
horsemen = ['war', 'famine', 'pestilence', 'death']

# Check if 'pestilence' is in the list
print('pestilence' in horsemen)  # Output: True

# Check if 'debauchery' is in the list
print('debauchery' in horsemen)  # Output: False
