# STRING METHODS

# 1. Capitalize()
# Converts the first character to upper case
x = "hello, World!"
print(x.capitalize())  # returns Hello, World!

# 2. casefold()
# converts string into lower case
x = "HELLO, World!"
print(x.casefold())  # returns hello, world!

# 3. center()
# Returns a centered string
x = "Hello, World!"
print(x.center())
