#!/usr/bin/python3

"""
This script demonstrates changing the case of a string using string methods.

1. The `title()` method capitalizes the first letter of each word.
2. The `upper()` method converts the entire string to uppercase.
3. The `lower()` method converts the entire string to lowercase.
"""

# Example usage
name = "ada lovelace"
print(name.title())

name = "Ada Lovelace"
print(name.upper())
print(name.lower())

